string|'"""\nList endpoints for an object, account or container.\n\nThis middleware makes it possible to integrate swift with software\nthat relies on data locality information to avoid network overhead,\nsuch as Hadoop.\n\nAnswers requests of the form::\n\n    /endpoints/{account}/{container}/{object}\n    /endpoints/{account}/{container}\n    /endpoints/{account}\n\nwith a JSON-encoded list of endpoints of the form::\n\n    http://{server}:{port}/{dev}/{part}/{acc}/{cont}/{obj}\n    http://{server}:{port}/{dev}/{part}/{acc}/{cont}\n    http://{server}:{port}/{dev}/{part}/{acc}\n\ncorrespondingly, e.g.::\n\n    http://10.1.1.1:6000/sda1/2/a/c2/o1\n    http://10.1.1.1:6000/sda1/2/a/c2\n    http://10.1.1.1:6000/sda1/2/a\n\nThe \'/endpoints/\' path is customizable (\'list_endpoints_path\'\nconfiguration parameter).\n\nIntended for consumption by third-party services living inside the\ncluster (as the endpoints make sense only inside the cluster behind\nthe firewall); potentially written in a different language.\n\nThis is why it\'s provided as a REST API and not just a Python API:\nto avoid requiring clients to write their own ring parsers in their\nlanguages, and to avoid the necessity to distribute the ring file\nto clients and keep it up-to-date.\n\nNote that the call is not authenticated, which means that a proxy\nwith this middleware enabled should not be open to an untrusted\nenvironment (everyone can query the locality data using this middleware).\n"""'
